					North India lies mainly in the north temperate zone of the Earth. Though cool or cold winters, hot summers and moderate monsoons are the general pattern. North India is one of the most climatically diverse regions on Earth. During summer, the temperature often rises above 35°C across much of the Indo-Gangetic plain, reaching as high as 50°C in the Thar desert, Rajasthan and up to 49°C in Delhi. During winter, the lowest temperature on the plains dips to below 5°C, and below the freezing point in some states. Heavy to moderate snowfall occurs in Himachal Pradesh, J&K and Uttarakhand. Much of North India is notorious for heavy fog during winters.

